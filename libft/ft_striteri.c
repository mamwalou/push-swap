#include "Includes/libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int n, char *))
{
	int i;

	if (!s || !f)
		return ;
	i = 0;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
